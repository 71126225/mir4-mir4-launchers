<h2>Q: Do I have to log in through a social media account to play AVA MIR4?<br>
A: No! unlike Global, you do NOT need to have a social media account (or steam) to start playing.
<br><br>
Q: How many accounts can I create?<br>
A: At the time of writing, no account creation limit has been enforced.
<br><br>
Q: How many game clients can I play/launch at the same time?<br>
A: A player can run up to 4 clients concurrently:<br>
<br>
- One game client per mobile device (no matter if Android or iOS, can't be the same account in use on another client)<br>
- Two game clients per pc through the standalone MIR4 launcher (must be two different accounts to use at the same time)<br>
- One game client through the Steam launcher on the same pc and concurrently with the two MIR4 launcher clients (can only log in to one steam account on one pc at a time and thus can only log into the WeMade account associated with that steam account on one pc at a time)<br>
- Virtual Machines, emulators and multi-client applications are expressly forbidden in this policy but multiple devices are not. That being said, in my opinion, the spirit of the policy seems to be that a single player may run up to 4 clients concurrently on multiple devices.<br>
<br><br>
Q: How many accounts can be used per IP address?<br>
A: There is no limit at this time but this may change, if it is found to be exploited.
<br><br>
Q: Can I play on different servers with the same account at the same time?<br>
A: No! You can not log in with the same account on different servers at the same time or play with two characters of the same account at the same time. Instead, you can create new accounts and play them concurrently as mentioned before.
<br><br>
Q: Can I play using an emulator like BlueStacks, LDPlayer or similar?<br>
A: Although an Android version is planned for future release, it is unclear at this time what emulators, if any, will be supported. As the potential for cheating is significantly increased through the use of emulation, it is unlikely that AVA Mir 4 will support emulation.
<br><br>
Q: Am I allowed to use VPN while playing?<br>
A: At the time of writing, There are no Geographical IP Restrictions for players connecting to the game and you can use a VPN of your choice. However, please be aware that VPN connections add additional data and computing requirements and may cause network or graphical performance issues on slower internet connections and depending on your VPN provider.
<br><br>
